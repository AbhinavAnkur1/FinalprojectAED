        private void populateMedicineTable() {
        DefaultTableModel model = (DefaultTableModel) testsTable.getModel();

        model.setRowCount(0);

        for (Medicine t : ecoSystem.getPharma().getmedList()) {

            
                Object[] row = new Object[4];                
                row[0] = t;
                row[1] = t.getMedUsage();
                row[2] =t.getMedQuant();
                row[3] = t.getMedPrice();
                model.addRow(row);
            

        }
    }
