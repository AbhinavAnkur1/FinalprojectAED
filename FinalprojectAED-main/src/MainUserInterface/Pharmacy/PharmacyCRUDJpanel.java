    private void addMedBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addMedBtnActionPerformed

        if (medNameTxt.getText().equals("") || medUsageTxt.getText().equals("")|| medPriceTxt.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please fill all the details");
        } else {
            Medicine m = new Medicine();
            m.setMedName(medNameTxt.getText());
            m.setMedQuant(Integer.parseInt(quantityTxt.getText()));
            m.setMedUsage(medUsageTxt.getText());
            m.setMedPrice(Integer.parseInt(medPriceTxt.getText()));
            ecoSystem.getPharma().createMedicine(m);

            populateMedicineTable();

        }

        medNameTxt.setText("");
        medUsageTxt.setText("");
        medPriceTxt.setText("");
        quantityTxt.setText("");
    }//GEN-LAST:event_addMedBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = testsTable.getSelectedRow();
        if(selectedRow>=0){
            int selectionButton = JOptionPane.YES_NO_OPTION;
            int selectionResult = JOptionPane.showConfirmDialog(null, "Delete Medicine: "+testsTable.getValueAt(selectedRow, 0)+" ??","Warning",selectionButton);
            if(selectionResult == JOptionPane.YES_OPTION){

                Medicine p = (Medicine) testsTable.getValueAt(selectedRow, 0);
                ecoSystem.getPharma().deleteMed(p);
                populateMedicineTable();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Select a Customer to delete!");
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:

        String name = medNameTxt.getText();
        String uname=medUsageTxt.getText();
        String password=medPriceTxt.getText();
        String quantity= quantityTxt.getText();
        if(name.equalsIgnoreCase("")||uname.equalsIgnoreCase("")||password.equalsIgnoreCase(""))
        {
            JOptionPane.showMessageDialog(null, "Pls, Fill data Correctly!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        else{

            ecoSystem.getPharma().updateMeds(m,name,uname,Integer.parseInt(password),Integer.parseInt(quantity));
            populateMedicineTable();
        }
        medNameTxt.setText("");
        medUsageTxt.setText("");
        medPriceTxt.setText("");
        quantityTxt.setText("");
    }//GEN-LAST:event_saveBtnActionPerformed

